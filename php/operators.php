<?php
// Arithmetic Operator
$a = 10;
$b = 20;
$c = $a + $b;
echo "Addition: " . $c;
echo "<br>";

// Comparison Operator
$e = 10;
$f = 20;

if ($e > $f) {
    echo "$e is big";
} else {
    echo "$f is big";
}
echo "<br>";

// Logical Operator
$g = 10;
$h = 20;

if ($g != $h) {   // logical condition
    echo "$g and $h are not equal";
} else {
    echo "$g and $h are equal";
}
?>
